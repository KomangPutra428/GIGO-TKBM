package com.tvip.getingetout.pojos;

public class keteranganmodel {
    String kat, lokasi, time;


    public keteranganmodel(String kat, String lokasi, String time) {
        this.kat = kat;
        this.lokasi = lokasi;
        this.time = time;



    }

    public String getKat() {
        return kat;
    }

    public String getLokasi() {
        return lokasi;
    }

    public String getTime() {
        return time;
    }
}





